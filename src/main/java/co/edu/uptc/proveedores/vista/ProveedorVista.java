    private void mostrarProveedores() {
        this.modeloTablaProveedor.setRowCount(0);
        this.modeloTablaProveedor.setColumnCount(3);
        List<Proveedor> proveedores = this.proveedorDAO.consultar();
        for (Proveedor proveedor : proveedores) {
            this.modeloTablaProveedor.addRow(new Object[]{proveedor.getCodigo(),
                proveedor.getDescripcion(), proveedor.getTelefono()});
        }
    }

