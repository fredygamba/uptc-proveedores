    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Proveedor proveedor = new Proveedor();
        proveedor.setCodigo((Integer) inputCodigo.getValue());
        proveedor.setContacto(inputContacto.getText());
        proveedor.setDescripcion(inputDescripcion.getText());
        proveedor.setPaginaWeb(inputPaginaWeb.getText());
        proveedor.setTelefono(inputTelefono.getText());
        this.proveedorDAO.agregar(proveedor);
        this.limpiarFormulario();
        this.mostrarProveedores();
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        Integer codigo = (Integer) inputCodigo.getValue();
        Proveedor proveedor = this.proveedorDAO.consultar(codigo);
        if (proveedor != null) {
            inputContacto.setText(proveedor.getContacto());
            inputDescripcion.setText(proveedor.getDescripcion());
            inputPaginaWeb.setText(proveedor.getPaginaWeb());
            inputTelefono.setText(proveedor.getTelefono());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        Proveedor proveedor = new Proveedor();
        proveedor.setCodigo((Integer) inputCodigo.getValue());
        proveedor.setContacto(inputContacto.getText());
        proveedor.setDescripcion(inputDescripcion.getText());
        proveedor.setPaginaWeb(inputPaginaWeb.getText());
        proveedor.setTelefono(inputTelefono.getText());
        this.proveedorDAO.actualizar(proveedor);
        this.limpiarFormulario();
        this.mostrarProveedores();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Integer codigo = (Integer) inputCodigo.getValue();
        DBUtils.delete(Proveedor.class, codigo);
        this.mostrarProveedores();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void limpiarFormulario() {
        inputCodigo.setValue(0);
        inputContacto.setText("");
        inputDescripcion.setText("");
        inputPaginaWeb.setText("");
        inputTelefono.setText("");
    }

